    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nama = txtNama.getText();
        String jk = " ";
         if (rdLaki.isSelected()){
         jk = "Laki-Laki";
        }else if (rdPerempuan.isSelected()) {
         jk = "Perempuan";
        }
        
        String angkatan = cbAngkatan.getSelectedItem().toString();
        String jurusan = cbJurusan.getSelectedItem().toString();
        String status = " ";
         if (rdAktif.isSelected()){
         status = "Aktif";
        }else if (rdTidakAktif.isSelected()){
         status = "Tidak Aktif";
        }
        String NIM = txtNIM.getText();

        txtHistory.append("====================================================\n");
        txtHistory.append("                                                      == SIDMA ==\n");
        txtHistory.append("                                    == Sistem Input Data Mahasiswa ==\n");
        txtHistory.append("====================================================\n");

        txtHistory.append("NIM \t: "+txtNIM.getText()+"\n");
        txtHistory.append("Nama \t: "+txtNama.getText()+"\n");
        txtHistory.append("Angkatan \t: "+cbAngkatan.getSelectedItem()+"\n");
        txtHistory.append("Kelas \t: "+cbJurusan.getSelectedItem()+"\n");
        txtHistory.append("Jenis Kelamin \t: "+jk+"\n");
        txtHistory.append("Status \t: "+status+"\n");

       
        mod = (DefaultTableModel) tableSiswa.getModel();
        Object[] data = new Object[6];
        data[0]=NIM;
        data[1]=nama;
        data[2]=angkatan;
        data[3]=jurusan;
        data[4]=jk;
        data[5]=status;
        mod.addRow(data);
              
        TableRowSorter trs = new TableRowSorter(mod);
        tableSiswa.setRowSorter(trs);

    }//GEN-LAST:event_jButton1ActionPerformed

