    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableSiswa.getModel();
        if (tableSiswa.getSelectedRowCount() == 1) {
            
                    String NIM = txtNIM.getText();
                    String nama = txtNama.getText();
                    String angkatan = cbAngkatan.getSelectedItem().toString();
                    String jurusan = cbJurusan.getSelectedItem().toString();
                    String jk = " ";
                     if (rdLaki.isSelected()){
         jk = "Laki-Laki";
        }else if (rdPerempuan.isSelected()) {
         jk = "Perempuan";
        }
                    String status = " ";
        if (rdAktif.isSelected()){
         status = "Aktif";
        }else if (rdTidakAktif.isSelected()){
         status = "Tidak Aktif";
        }
                    
                   model.setValueAt(NIM, tableSiswa.getSelectedRow(), 0);
                   model.setValueAt(nama, tableSiswa.getSelectedRow(), 1);
                   model.setValueAt(angkatan, tableSiswa.getSelectedRow(), 2);
                   model.setValueAt(jurusan, tableSiswa.getSelectedRow(), 3);
                   model.setValueAt(jk, tableSiswa.getSelectedRow(), 4);
                   model.setValueAt(status, tableSiswa.getSelectedRow(), 5);

        
                   
                   JOptionPane.showMessageDialog(this, "Edit Sukses");
        } else if (tableSiswa.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tabel kosong");
            
        } else {
            JOptionPane.showMessageDialog(this, "Pilih salah satu baris tabel!");
        }
    }//GEN-LAST:event_btnEditActionPerformed
    
